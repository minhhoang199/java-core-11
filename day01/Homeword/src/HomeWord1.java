public class HomeWord1 {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        //a + b
        System.out.println(a + b);

        //a-b
        System.out.println(a - b);

        //b-a
        System.out.println(b - a);

        //a*b
        System.out.println(a * b);

        //a/b
        System.out.println(a / b);

        //b/a
        System.out.println(b / a);
    }
}
